x = 10
y = 3

# penjumlahan
result = x + y
print(result)
print()

# pengurangan
result = x - y
print(result)
print()

# pembagian
result = x / y
print(result)
print()

# perkalian
result = x * y
print(result)
print()

# eksponen/pangkat
result = x ** y
print(result)
print()

# floor division
result = x // y
print(result)
print()

# modulo
result = x % y
print(result)
print()

print("\n==========\n")

# operator precedence
x = 5 * (31 + 2) // 4 ** 2
print(x)
